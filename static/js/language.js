  Language.init = function () {
    this.Locale = navigator.language.toLocaleLowerCase();
    $('[i18n]').each(function () {
      var items = $(this).attr('i18n').split(';');
      items.forEach((item) => {
        var arr = item.split(':');
        var attr = arr[0];
        var key = arr[1];
        var val = Language.getLang(key);
        if (attr === 'content') {
          $(this).html(val);
        } else {
          $(this).attr(attr, val);
        }
      });
    });
  };

